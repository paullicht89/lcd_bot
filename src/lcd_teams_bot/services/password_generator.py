def generate_password() -> str:
    word1 = _random_word()
    word2 = _random_word()
    word3 = _random_word()
    number = secrets.randbelow(90) + 10
    symbol = secrets.choice(SYMBOLS)

    return f"{word1}-{word2}-{word3}-{number}{symbol}"


def _random_word() -> str:
    return secrets.choice(WORDS).capitalize()
